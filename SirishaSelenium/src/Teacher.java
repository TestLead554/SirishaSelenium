
public class Teacher {

	
        int id;
        String name;
        int sal;
        
        void setId(int id)
        {
        	this.id = id;
        }
        void setName(String name)
        {
        	this.name = name;
        }
        void setSal(int sal)
        {
        	this.sal = sal;
        }
        int getId()
        {
        	return id;
        }
        String getName()
        {
        	return name;
        }
        int getSal()
        {
        	return sal;
        }
	}


