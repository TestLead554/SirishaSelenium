
public class Student {
	int id;
    String name;
    int marks;
    
    void setId(int id)
    {
    	this.id = id;
    }
    void setName(String name)
    {
    	this.name = name;
    }
    void setMarks(int marks)
    {
    	this.marks = marks;
    }
    int getId()
    {
    	return id;
    }
    String getName()
    {
    	return name;
    }
    int getMarks()
    {
    	return marks;
    }

}
