package heirarchicalinheritanceexample;

public class College {
	
	int id;
	String name;
	String address;
	
	void setId(int id)
	{
		this.id = id;
	}
	int getId()
	{
		return id;
	}
	
	void setName(String name)
	{
		this.name = name;
	}
	String getName()
	{
		return name;
	}
	
	void setAddress(String address)
	{
		this.address = address;
	}
	String getAddress()
	{
		return address;
	}

}
